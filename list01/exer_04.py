#Faça um Programa que peça as 4 notas bimestrais e mostre a média. 
print("Atenção: as notas deveram ser informadas de 1 - 10")
n1 = float(input("Informea nota do bimestre 1:"))
n2 = float(input("Informea nota do bimestre 2:"))
n3 = float(input("Informea nota do bimestre 3:"))
n4 = float(input("Informea nota do bimestre 4:"))

m = (n1+n2+n3+n4)/4

print("Média = ",m)